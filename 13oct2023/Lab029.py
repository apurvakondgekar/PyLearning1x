#Arithmetic Operators
# + Addition
# - Subtraction
# * Multiplication
# / Division
# % Modulus (remainder)
# ** Exponential
# // Floor division (returns the quotient, discarding the remainder)

# Take value from user
a = int(input("Enter the value of a\n"))
b = int(input("Enter the value of b\n"))

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)
print(a // b)
print(a % b)


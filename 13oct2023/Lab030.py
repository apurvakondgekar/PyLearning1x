# Comparison Operators:
# == Equal to
# != Not equal to
# < Less than
# > Greater than
# <= Less than or equal to
# >= Greater than or equal to

x = 5
y = 10

print(x,"==",y,x == y) # Bool - false or true
print(x,"!=",y,x != y)
print(x,">",y,x > y)
print(x,"<",y,x < y)
print(x,"<=",y,x <= y)
print(x,">=",y, x >= y)

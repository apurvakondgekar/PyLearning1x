'''
len(str): Returns the length of the string.

str(): Converts an object to a string.

ord(char): Returns the Unicode code point (integer) for a given character.

chr(code): Converts a Unicode code point to its corresponding character.

max(str): Returns the character with the highest Unicode code point in the string.

min(str): Returns the character with the lowest Unicode code point in the string.

str.split([sep[, maxsplit]]): Splits the string into a list of substrings based on the specified separator (default is whitespace) with an optional maxsplit parameter.

str.rsplit([sep[, maxsplit]]): Splits the string into a list of substrings from the right.

str.join(iterable): Joins the elements of an iterable into a single string, using the string as a separator.

str.upper(): Converts the string to uppercase.

str.lower(): Converts the string to lowercase.

str.capitalize(): Capitalizes the first character of the string.

str.title(): Converts the string to title case.

str.strip([characters]): Removes leading and trailing characters (whitespace by default).

str.lstrip([characters]): Removes leading characters.

str.rstrip([characters]): Removes trailing characters.

str.startswith(prefix[, start[, end]]): Checks if the string starts with the specified prefix.

str.endswith(suffix[, start[, end]]): Checks if the string ends with the specified suffix.

str.count(substring[, start[, end]]): Counts the occurrences of a substring in the string.

str.find(substring[, start[, end]]): Finds the first occurrence of a substring in the string and returns its index, or -1 if not found.

str.rfind(substring[, start[, end]]): Finds the last occurrence of a substring in the string and returns its index, or -1 if not found.

str.replace(old, new[, count]): Replaces occurrences of the "old" substring with the "new" substring. The optional count parameter limits the number of replacements.

str.zfill(width): Pads the string with zeros to the specified width.

str.center(width[, fillchar]): Centers the string in a field of the given width, optionally padding with a specified character.

str.ljust(width[, fillchar]): Left-justifies the string in a field of the given width, optionally padding with a specified character.

str.rjust(width[, fillchar]): Right-justifies the string in a field of the given width, optionally padding with a specified character.

str.encode([encoding[, errors]]): Encodes the string into bytes using the specified encoding (e.g., 'utf-8').

str.decode([encoding[, errors]]): Decodes bytes to a string using the specified encoding.
'''